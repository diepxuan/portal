namespace Diepxuan\Simba\StoredProcedures;

use Diepxuan\Simba\SModel\SModel;
use Illuminate\Support\Collection;

class AsPOInsDMCP
{
    /**
     * Call stored procedure asPOInsDMCP
     *
     * @param array $params Procedure parameters
     * @return Collection
     */
    public static function call(array $params): Collection
    {
        $connection = (new SModel())->getConnectionName();

        return ProcedureCaller::call('asPOInsDMCP', $params, $connection);
    }

    /**
     * Call stored procedure asPOInsDMCP with named parameters
     *
     * @param string $Ma_cty
     * @param string $Ma_cp
     * @param string $Ten_cp
     * @param string $Tt_pb
     * @param bool $Ksd
     * @param string $LUser
     * @param int $Ret
     * @return Collection
     */
    public static function callWithParams(string $Ma_cty = null, string $Ma_cp = null, string $Ten_cp = null, string $Tt_pb = null, bool $Ksd = null, string $LUser = null, int $Ret = null): Collection
    {
        $params = [
            'pMa_cty' => $Ma_cty,
            'pMa_cp' => $Ma_cp,
            'pTen_cp' => $Ten_cp,
            'pTt_pb' => $Tt_pb,
            'pKsd' => $Ksd,
            'pLUser' => $LUser,
            'pRet' => $Ret
        ];
        
        return self::call($params);
    }
}
