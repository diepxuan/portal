	private string GetFilterTitle()
	{
		string text = "";
		if (Operators.CompareString(txtSo_Ct1.Text.Trim(), "", false) != 0)
		{
			text = text + lblSo_Ct1.Text + ": " + txtSo_Ct1.Text.Trim();
		}
		if (Operators.CompareString(txtSo_Ct2.Text.Trim(), "", false) != 0)
		{
			text = text + lblSo_Ct2.Text + ": " + txtSo_Ct2.Text.Trim();
		}
		if (cboGiao_dich.SelectedIndex != -1)
		{
			text = text + lblGiao_dich.Text + ": " + cboGiao_dich.Text + "; ";
		}
		if (Operators.CompareString(((TextBox)txtMa_Kh).Text.Trim(), "", false) != 0)
		{
			text = text + lblMa_Kh.Text + ": " + ((TextBox)txtMa_Kh).Text + "; ";
		}
		if (Operators.CompareString(((TextBox)txtMa_NhKh).Text.Trim(), "", false) != 0)
		{
			text = text + lblMa_NhKh.Text + ": " + ((TextBox)txtMa_NhKh).Text + "; ";
		}
		if (Operators.CompareString(((TextBox)txtMa_plkh1).Text.Trim(), "", false) != 0)
		{
			text = text + lblPLKh1.Text + ": " + ((TextBox)txtMa_plkh1).Text + "; ";
		}
		if (Operators.CompareString(((TextBox)txtMa_plkh2).Text.Trim(), "", false) != 0)
		{
			text = text + lblPLKh2.Text + ": " + ((TextBox)txtMa_plkh2).Text + "; ";
		}
		if (Operators.CompareString(lblPLKh3.Text.Trim(), "", false) != 0)
		{
			text = text + lblPLKh3.Text + ": " + lblPLKh3.Text + "; ";
		}
		if (Conversions.ToBoolean(ChkSap_xep.Checked))
		{
			text = text + lblSap_xep.Text + ": " + ((ButtonBase)ChkSap_xep).Text + "; ";
		}
