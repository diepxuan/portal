		if (!EditMode)
		{
			radTk.Checked = true;
		}
		if (Operators.CompareString(((TextBox)txtTk).Text.Trim(), "", false) == 0)
		{
			if (Operators.CompareString(txtCach_tinh.Text.Trim(), "", false) == 0)
			{
				if (Operators.CompareString(((TextBox)txtMa_ref).Text.Trim(), "", false) == 0)
				{
					radTk.Checked = true;
					radTk_Click(null, null);
				}
				else
				{
					radChi_tieu.Checked = true;
					radChi_tieu_Click(null, null);
				}
			}
			else
			{
				radCong_thuc.Checked = true;
				radCong_thuc_Click(null, null);
			}
		}
		else
		{
			radTk.Checked = true;
			radTk_Click(null, null);
		}
	}

