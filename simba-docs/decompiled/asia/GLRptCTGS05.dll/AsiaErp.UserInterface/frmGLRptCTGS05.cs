	private void InitNgayLo()
	{
		((ComboBox)cboNgay_lo).DataSource = AsiaErp.Framework.Environment.GetKyBaoCao(AsiaErp.Framework.Environment.GetSIConfiguration().Ngay_DnTc);
		((ListControl)cboNgay_lo).ValueMember = "ma";
		((ListControl)cboNgay_lo).DisplayMember = "ten_ky";
		if (Operators.ConditionalCompareObjectNotEqual(Helper.Repl_Null(RuntimeHelpers.GetObjectValue(AsiaErp.Framework.Environment.get_PublicVariables("KyBaoCao")), isNum: false), (object)"", false))
		{
			((ListControl)cboNgay_lo).SelectedValue = RuntimeHelpers.GetObjectValue(AsiaErp.Framework.Environment.get_PublicVariables("KyBaoCao"));
		}
		else
		{
			((ComboBox)cboNgay_lo).SelectedIndex = 0;
		}
	}

	private void cboKyTruoc_SelectedValueChanged(object sender, EventArgs e)
	{
		if (((ComboBox)cboNgay_lo).DataSource != null)
		{
			if (Operators.CompareString(((KyBaoCao)((ComboBox)cboNgay_lo).SelectedItem).ma, "NGAY", false) != 0)
			{
				AsMaskedTextBox asMaskedTextBox = txtNgay_lo1;
				DateTime dateTime = new DateTime(SystemInformations.FinancialYear, ((KyBaoCao)((ComboBox)cboNgay_lo).SelectedItem).thang1, 1);
				asMaskedTextBox.Value = dateTime;
				AsMaskedTextBox asMaskedTextBox2 = txtNgay_lo2;
				dateTime = new DateTime(SystemInformations.FinancialYear, ((KyBaoCao)((ComboBox)cboNgay_lo).SelectedItem).thang2, 1);
				DateTime dateTime2 = dateTime;
				asMaskedTextBox2.Value = dateTime2.AddMonths(1).AddDays(-1.0);
				((MaskedTextBox)txtNgay_lo1).ReadOnly = true;
				((Control)txtNgay_lo1).TabStop = false;
				((MaskedTextBox)txtNgay_lo2).ReadOnly = true;
				((Control)txtNgay_lo2).TabStop = false;
			}
			else
			{
				((MaskedTextBox)txtNgay_lo1).ReadOnly = false;
				((Control)txtNgay_lo1).TabStop = true;
				((MaskedTextBox)txtNgay_lo2).ReadOnly = false;
				((Control)txtNgay_lo2).TabStop = true;
			}
		}
	}
